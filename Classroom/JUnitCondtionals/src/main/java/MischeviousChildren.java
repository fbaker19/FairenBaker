public class MischeviousChildren {

    public boolean AreWeInTrouble(boolean aSmile, boolean bSmile) {

        if (aSmile == bSmile) {
            return true;
        } else if (aSmile != bSmile) {
            return false;
        } else {
            return true;
        }

    }
}
